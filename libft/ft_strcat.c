char	*ft_strcat(char *dst, const char *src)
{
	int	i;
	int	g;

	i = 0;
	g = 0;
	while (dst[i])
		i++;
	while (src[g])
	{
		dst[i] = src[g];
		i++;
		g++;
	}
	dst[i] = '\0';
	return (dst);
}
