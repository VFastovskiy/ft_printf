#include <string.h>

char				*ft_strchr(const char *s, int c)
{
	char			x;
	int				i;

	i = 0;
	x = (char)c;
	while (s[i] != x)
	{
		if (!s[i])
			return (NULL);
		i++;
	}
	return (&((char *)s)[i]);
}
