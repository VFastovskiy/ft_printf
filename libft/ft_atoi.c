int							ft_atoi(const char *str)
{
	int					i;
	int					m;
	unsigned long long	r;

	i = 0;
	m = 1;
	r = 0;
	while (str[i] == '\t' || str[i] == '\n' || str[i] == '\v'\
			|| str[i] == '\f' || str[i] == '\r' || str[i] == ' ')
		i++;
	if (str[i] == '-')
		m = -1;
	if (str[i] == '-' || str[i] == '+')
		i++;
	while (str[i] >= '0' && str[i] <= '9')
	{
		r = r * 10 + str[i] - '0';
		i++;
	}
	if (i > 19 || r > 9223372036854775807)
		return (m == 1 ? -1 : 0);
	return (r * m);
}
